def generate_counts(duration_seconds):
    # Generate view_count
    # Using a log-normal distribution for a more realistic "long tail" of views.
    # This will generate views ranging from thousands to millions, with a few very popular ones.
    base_views = int(random.lognormvariate(10, 1.5))

    # Adjust views based on duration, giving slightly more weight to longer videos but keeping randomness.
    # A short video (e.g., 60s) might get a factor around 0.5-1.5, a long video (e.g., 1800s) around 1-3.
    duration_factor = 1 + (duration_seconds / 3600.0) * random.uniform(0.5, 2.5) # Max duration is 3600s (1 hour)
    view_count = int(base_views * duration_factor)

    # Ensure view_count is within a reasonable range (e.g., 100 to 50 million)
    view_count = max(100, min(view_count, 50_000_000))

    # Generate like_count
    # Likes typically range from 1% to 15% of views
    like_ratio = random.uniform(0.01, 0.15)
    like_count = int(view_count * like_ratio)
    like_count = max(0, like_count) # Ensure non-negative

    # Generate comment_count
    # Comments typically range from 0.1% to 1% of views
    comment_ratio = random.uniform(0.001, 0.01)
    comment_count = int(view_count * comment_ratio)
    comment_count = max(0, comment_count) # Ensure non-negative

    return view_count, like_count, comment_count

